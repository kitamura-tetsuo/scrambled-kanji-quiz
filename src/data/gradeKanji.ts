export function getStrokeCount(part: string): number {
  if (!part) return 0;
  let total = 0;
  for (const ch of part) {
    total += getSingleCharStrokeCount(ch);
  }
  return total;
}

export function getSingleCharStrokeCount(ch: string): number {
  if (COMPONENT_STROKES[ch] !== undefined) {
    return COMPONENT_STROKES[ch];
  }
  const code = ch.charCodeAt(0);
  if (code >= 0x4e00 && code <= 0x9fff) {
    return 8;
  }
  if (code >= 0x3040 && code <= 0x30ff) {
    return 2;
  }
  return 2;
}

export function optimizeParts(parts: string[], limitLength: number = 1): string[] {
  return mergeToNParts(parts, limitLength);
}

export function mergeToNParts(parts: string[], N: number): string[] {
  let list = [...parts];
  
  while (list.length > N) {
    let bestMergeIdx = 0;
    let minPenalty = Infinity;
    
    for (let i = 0; i < list.length - 1; i++) {
      const candidateList = [...list];
      candidateList.splice(i, 2, list[i] + list[i+1]);
      
      const strokes = candidateList.map(p => getStrokeCount(p));
      const minStroke = Math.min(...strokes);
      const maxStroke = Math.max(...strokes);
      
      // 1. 1画パーツの数に対するペナルティ
      const oneStrokeCount = strokes.filter(s => s <= 1).length;
      
      // 2. 画数の最大・最小の差（ばらつき）に対するペナルティ
      const variance = maxStroke - minStroke;
      
      const penalty = (oneStrokeCount * 1000) + variance;
      
      if (penalty < minPenalty) {
        minPenalty = penalty;
        bestMergeIdx = i;
      }
    }
    
    list.splice(bestMergeIdx, 2, list[bestMergeIdx] + list[bestMergeIdx+1]);
  }
  
  return list;
}

